<div class="article_memo" data-title="COMMENT">
  <p class="inner_text">First, you need to create your account to play MHF. Some servers can create automatically an account by entering username and password in the launcher and logging in. However, to prevent <span style="font-weight: 800;">suspicious person and cheaters</span> from coming to Rain Server, automatic creating function from the launcher is disable. Therefore, please create your account in either one of the two way below.</p>
