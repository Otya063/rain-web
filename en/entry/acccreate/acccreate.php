  <h2>Create on Rain Official Discord</h2>
  <ul>
    <li class="itemset">
      <p>(1)　After entering /create in any channel, you can see input fields for "username" and "password" by typing a space or selecting /create from the list of commands. <br>You can enter any value you want. However, be sure to remember them.
      </p>
      <p>
        <img src="/manual/en/entry/acccreate/acccreate_1.png" width="500" height="156" />
      </p>
    </li>
    <li class="itemset">
      <p>(2)　After entering your information, the message below will be displayed indicating that tentative account creation is complete.</p>
      <p>
        <img src="/manual/en/entry/acccreate/acccreate_2.png" width="500" height="168" />
      </p>
    </li>
    <li class="itemset">
      <p>(3)　After running the launcher, enter your username and password set in (2) and press "LOG IN."</p>
      <p>
        <img src="/manual/en/entry/acccreate/acccreate_3.png" width="500" height="265" />
      </p>
    </li>
    <li class="itemset">
      <p>(4)　Nothing is displayed about account information; however, this is normal.<br>When you go forward to [Create New Character] > [Buy now] > [Refresh], you can see <span style="color: blue;">Ready to Hunt</span>.</p>
      <p>
        <img src="/manual/en/entry/acccreate/acccreate_4_1.png" width="500" height="265" />
