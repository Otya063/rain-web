      <p>
        <img src="/manual/en/entry/acccreate/acccreate_5.png" width="500" height="281" />
      </p>
    </li>
    <li class="itemset">
      <p>(6)　If you can move to Mezeporta Square successfully, your account creation is complete.</p>
      <p>
